export function getTranslation(language: string, key: string, params?: Record<string, string | number>): string {
  const lang = language in translations ? language : "en-US"
  let text = (translations as any)[lang][key] || (translations as any)["en-US"][key] || key

  // Replace parameters
  if (params) {
    Object.keys(params).forEach((param) => {
      text = text.replace(`{${param}}`, String(params[param]))
    })
  }

  return text
}
